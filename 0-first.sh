#!/bin/sh
# This is a comment

# The first line is shebang '#!'. It is a character sequence
# interpreted as executable file. Tells the program loader to
# run the script using /bin/sh . Example - for python scripts
# we use '#!/usr/bin/python3'
# Read More at https://en.wikipedia.org/wiki/Shebang_(Unix)

echo "Hello     World"
echo "Hello world"
echo "Hello * world"	# This is also a comment
echo Hello * world
echo Hello 	world
echo "Hello" world
echo "hello " " world"
echo "hello "*" world"
echo `hello` world
echo 'hello' world
